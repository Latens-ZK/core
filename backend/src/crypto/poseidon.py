def _mix3(s0, s1, s2):
    t = (s0 + s1 + s2) % P
    return (t + 2 * s0) % P, (t - 2 * s1) % P, (t - 3 * s2) % P


def _hades_permutation(s0, s1, s2):
    idx = 0
    for _ in range(_RF // 2):
        s0 = pow((s0 + _RC[idx])     % P, 3, P)
        s1 = pow((s1 + _RC[idx + 1]) % P, 3, P)
        s2 = pow((s2 + _RC[idx + 2]) % P, 3, P)
        s0, s1, s2 = _mix3(s0, s1, s2)
        idx += 3
    for _ in range(_RP):
        s2 = pow((s2 + _RC[idx]) % P, 3, P)
        s0, s1, s2 = _mix3(s0, s1, s2)
        idx += 1
    for _ in range(_RF // 2):
        s0 = pow((s0 + _RC[idx])     % P, 3, P)
        s1 = pow((s1 + _RC[idx + 1]) % P, 3, P)
        s2 = pow((s2 + _RC[idx + 2]) % P, 3, P)
        s0, s1, s2 = _mix3(s0, s1, s2)
        idx += 3
    return s0, s1, s2


def poseidon_hash(x: int, y: int) -> int:
    """Poseidon(x, y) -- matches Cairo poseidon_hash(x, y). State: [x, y, 2]."""
    s0, _, _ = _hades_permutation(x % P, y % P, 2)
    return s0


def poseidon_hash_single(x: int) -> int:
    s0, _, _ = _hades_permutation(x % P, 0, 1)
    return s0


def poseidon_hash_many(inputs) -> int:
    """Sponge hash of a list, rate=2, capacity=1."""
    values = [v % P for v in inputs]
    values.append(1)
    if len(values) % 2 != 0:
        values.append(0)
    s0, s1, s2 = 0, 0, 0
    for i in range(0, len(values), 2):
        s0 = (s0 + values[i]) % P
        s1 = (s1 + values[i + 1]) % P
        s0, s1, s2 = _hades_permutation(s0, s1, s2)
    return s0


class PoseidonHash:
    """Poseidon hash utilities -- Starknet Cairo compatible."""
    PRIME = P

    @staticmethod
    def hash(x: int, y: int) -> int:
        return poseidon_hash(x, y)

    @staticmethod
    def hash_many(inputs) -> int:
        return poseidon_hash_many(inputs)

    @staticmethod
    def hash_address_balance(address_hash: int, balance: int) -> int:
        """Leaf: Poseidon(address_hash, balance)."""
        return poseidon_hash(address_hash, balance)

    @staticmethod
    def hash_commitment(address_hash: int, salt: int) -> int:
        """Commitment: Poseidon(address_hash, salt)."""
        return poseidon_hash(address_hash, salt)
