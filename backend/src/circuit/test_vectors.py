# ─── Tests ─────────────────────────────────────────────────────────────────────

class TestPoseidonCrossCompatibility:
    """
    Validate Python Poseidon == Cairo hades_permutation(x, y, 2)[0]
    for all 10 test vectors.
    """

    @pytest.mark.parametrize("x,y,expected", _VECTORS)
    def test_poseidon_pair_hash_matches_expected(self, x, y, expected):
        """
        poseidon_hash(x, y) must equal the pre-computed value.
        If this fails, the round constants or MDS mix in poseidon.py are wrong.
        """
        result = poseidon_hash(x, y)
        assert result == expected, (
            f"poseidon_hash({hex(x)[:20]}, {hex(y)[:20]})\n"
            f"  got:      {result}\n"
            f"  expected: {expected}\n"
            "Round constants or MDS matrix may be mismatched."
        )

    @pytest.mark.parametrize("x,y,expected", _VECTORS)
    def test_PoseidonHash_hash_alias(self, x, y, expected):
        """PoseidonHash.hash(x, y) is an alias for poseidon_hash; must match."""
        assert PoseidonHash.hash(x, y) == expected

    @pytest.mark.parametrize("x,y,expected", _VECTORS)
    def test_hash_commitment_uses_pair_hash(self, x, y, expected):
        """PoseidonHash.hash_commitment(x, y) must also equal poseidon_hash(x, y)."""
        assert PoseidonHash.hash_commitment(x, y) == expected

    def test_all_outputs_in_field(self):
        """All outputs must be valid felt252 values (< PRIME)."""
        for x, y, expected in _VECTORS:
            result = poseidon_hash(x, y)
            assert 0 <= result < PRIME, f"Output {result} out of felt252 range"

    def test_hash_is_deterministic(self):
        """Same inputs always produce the same output."""
        x, y = 42, 1337
        first = poseidon_hash(x, y)
        for _ in range(5):
            assert poseidon_hash(x, y) == first

    def test_hash_is_not_commutative(self):
        """Poseidon(x, y) != Poseidon(y, x) in general (pair hash is ordered)."""
        x, y = 1, 2
        assert poseidon_hash(x, y) != poseidon_hash(y, x), (
            "Poseidon pair hash should NOT be commutative — "
            "direction matters for Merkle tree correctness"
        )

    def test_address_balance_leaf_consistency(self):
        """
        hash_address_balance(addr, bal) == poseidon_hash(addr, bal)
        This is what Cairo computes as the Merkle leaf.
        """
        addr_hash = 0xABC
        balance = 1000
        assert PoseidonHash.hash_address_balance(addr_hash, balance) == poseidon_hash(addr_hash, balance)

    def test_full_commitment_flow(self):
        """
        Simulate the full commitment derivation:
          commitment = Poseidon(SHA256(address) % PRIME, salt)
        Must match the Cairo contract and frontend starknet.js computation.
        """
        address = "1A1zP1eP5QGefi2DMPTfTL5SLmv7DivfNa"
        salt_hex = "deadbeefdeadbeefdeadbeefdeadbeefdeadbeefdeadbeefdeadbeefdeadbeef"

        # Replicate address_utils.py encoding
        h = hashlib.sha256(address.encode('utf-8')).digest()
        address_hash = int.from_bytes(h, 'big') % PRIME
        assert address_hash == _ADDR_HASH, "Address hash encoding mismatch"

        salt = int(salt_hex, 16) % PRIME
        assert salt == _SALT, "Salt encoding mismatch"

        commitment = PoseidonHash.hash_commitment(address_hash, salt)

        # Must be in field
        assert 0 < commitment < PRIME
        # Must match vector index 6
        assert commitment == _VECTORS[6][2], f"Commitment {commitment} != vector {_VECTORS[6][2]}"

    def test_felt252_encoding_idempotent(self):
        """All inputs reduced mod PRIME produce consistent results."""
        x = PRIME + 42  # out-of-range input
        y = 1337
        # Both should reduce to the same result as x % PRIME
        result_raw = poseidon_hash(x, y)
        result_reduced = poseidon_hash(x % PRIME, y)
        assert result_raw == result_reduced

    def test_ten_vectors_all_distinct(self):
        """All 10 expected outputs should be distinct (birthday attack sanity check)."""
        outputs = [expected for _, _, expected in _VECTORS]
        assert len(outputs) == len(set(outputs)), "Duplicate hash outputs — collision detected"


